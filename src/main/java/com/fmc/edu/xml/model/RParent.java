public class RParent {
    private String mStudentSerialNumber;
    private String mName;
    private String mStudentName;
    private String mPhone;
    private String mRelationship;

    public String getStudentSerialNumber() {
        return mStudentSerialNumber;
    }

    public void setStudentSerialNumber(String pStudentSerialNumber) {
        mStudentSerialNumber = pStudentSerialNumber;
    }

    public String getName() {
        return mName;
    }

    public void setName(String pName) {
        mName = pName;
    }

    public String getStudentName() {
        return mStudentName;
    }

    public void setStudentName(String pStudentName) {
        mStudentName = pStudentName;
    }

    public String getPhone() {
        return mPhone;
    }

    public void setPhone(String pPhone) {
        mPhone = pPhone;
    }

    public String getRelationship() {
        return mRelationship;
    }

    public void setRelationship(String pRelationship) {
        mRelationship = pRelationship;
    }
}
