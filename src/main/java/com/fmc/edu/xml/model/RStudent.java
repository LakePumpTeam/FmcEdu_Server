public class RStudent {
    private String mGrade;
    private String mCls;
    private String mMasterTeacherName;
    private String mStudentSerialNumber;
    private String mName;
    private String mSex;
    private String mBirthDay;

    public String getGrade() {
        return mGrade;
    }

    public void setGrade(String pGrade) {
        mGrade = pGrade;
    }

    public String getCls() {
        return mCls;
    }

    public void setCls(String pCls) {
        mCls = pCls;
    }

    public String getMasterTeacherName() {
        return mMasterTeacherName;
    }

    public void setMasterTeacherName(String pMasterTeacherName) {
        mMasterTeacherName = pMasterTeacherName;
    }

    public String getStudentSerialNumber() {
        return mStudentSerialNumber;
    }

    public void setStudentSerialNumber(String pStudentSerialNumber) {
        mStudentSerialNumber = pStudentSerialNumber;
    }

    public String getName() {
        return mName;
    }

    public void setName(String pName) {
        mName = pName;
    }

    public String getSex() {
        return mSex;
    }

    public void setSex(String pSex) {
        mSex = pSex;
    }

    public String getBirthDay() {
        return mBirthDay;
    }

    public void setBirthDay(String pBirthDay) {
        mBirthDay = pBirthDay;
    }
}
